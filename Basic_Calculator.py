num1 = input("Enter a number: ")
num2 = input("Enter another number: ")
# Input is ALWAYS stored as a STRING
# result =float(num1)+float(num2)
print("Sum of the number: ", (float(num1) + float(num2)))
print("Diff of the number: ", (float(num1) - float(num2)))
print("Product of the number: ", (float(num1) * float(num2)))
print("Remainder of the number: ", (float(num1) % float(num2)))
print("Quotient of the number: ", (float(num1) / float(num2)))
